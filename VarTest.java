package day03;

public class VarTest {

	public static void main(String[] args) {
		//정수형변수
		int i =100;
		//실수형변수
		float f = 3.14f;
        //문자형변수
		char c = 'c';
		//문자열변수
		String s = "hello";
		//논리혛변수
		boolean b = true;
		
		System.out.println(i);
		System.out.println(f);
		System.out.println(c);
		System.out.println(s);
		System.out.println(b);

	}

}
