public class Operator2 {
    public static void main(String[] args) {
        int var1 = 10, var2 = 3;

        boolean result = var1 > var2;
        System.out.println(result);
        result = var1 < var2;
        System.out.println(result);

        result = var1 >= var2;
        System.out.println(result);

        result = var1 <= var2;
        System.out.println(result);

        result = var1 == var2;
        System.out.println(result);

        result = var1 != var2;
        System.out.println(result);
    }
}
